<template>
  <div class="custom-date-time-picker">
    <div v-if="isDateTime">
      <v-datetime-picker
        v-model="date"
        :label="fieldCaption"
        :text-field-props="textFieldProps"
        @input="onCalendarChange"
      >
        <template slot="dateIcon">
          <v-icon>fas fa-calendar</v-icon>
        </template>
        <template slot="timeIcon">
          <v-icon>fas fa-clock</v-icon>
        </template>
      </v-datetime-picker>
    </div>
    <div v-else>
      <v-dialog
        ref="dialog"
        v-model="modal"
        :return-value.sync="date"
        persistent
        width="290px"
      >
        <template v-slot:activator="{ on, attrs }">
          <v-text-field
            v-model="date"
            :label="fieldCaption"
            :rules="rules"
            readonly
            outlined
            dense
            v-bind="attrs"
            v-on="on"
          ></v-text-field>
        </template>
        <v-date-picker v-model="date" scrollable @change="onCalendarChange">
          <v-spacer></v-spacer>
          <v-btn text color="primary" @click="modal = false">Cancel</v-btn>
          <v-btn text color="primary" @click="$refs.dialog.save(date)"
            >OK</v-btn
          >
        </v-date-picker>
      </v-dialog>
    </div>
  </div>
</template>

<script>
import { formFieldMixin } from '~/utility/form-control'
export default {
  mixins: [formFieldMixin],
  props: ['value', 'field', 'rules', 'onChange'],
  data() {
    const dateTime = this.value || new Date()
    const date =
      this.field.type === 'datetime'
        ? new Date(dateTime)
        : new Date(dateTime).toISOString().substr(0, 10)
    return {
      date,
      modal: false,
      textFieldProps: {
        appendIcon: 'fa-calendar',
        outlined: true,
        dense: true,
        rules: this.rules,
      },
    }
  },
  computed: {
    isDateTime() {
      return this.field.type === 'datetime'
    },
  },
  mounted() {
    this.onCalendarChange()
  },
  methods: {
    onCalendarChange() {
      this.$emit('input', this.date)
      this.onChange && this.onChange(this.date)
    },
  },
}
</script>
<style>
.v-picker {
  border-radius: 0;
}
</style>
